#include<string.h>
#include<stdlib.h>
#include<math.h>
#include<stdio.h>
int main()
{
char d1[100086],d2[100086],op;
int b1[100086],b2[100086],c1[100086];
int i,j,k,p,q,jiahao,d;
while(scanf("%s %c %s",&d1,&op,&d2)!=EOF)
{

	memset(b1,0,sizeof(b1));
	memset(b2,0,sizeof(b2));
	memset(c1,0,sizeof(c1));

	p=strlen(d1);
	q=strlen(d2);
	for(i=0;i<p;i++)
	{
		b1[i]=d1[p-i-1]-'0';
	}
	for(j=0;j<q;j++)
	{
		b2[j]=d2[q-j-1]-'0';
	}

	switch(op)
		{
		case'+':
			if(p>=q)
			{
				jiahao=p+1;
			}
			else
			{
				jiahao=q+1;
			}
			for(i=0;i<jiahao;i++)
			{
				c1[i]+=b1[i]+b2[i];
				if(c1[i]>=10)
				{
					c1[i]%=10;
					c1[i+1]+=1;
				}
			}
			d=jiahao-1;
			while (c1[d]==0)d--;
			{
				if(d<0)
				{
					printf("0");
				}
				else
				{
					for(;d>=0;d--)
					{
						printf("%d",c1[d]);
					}	
				}
				printf("\n");
			}
			continue;

		case'-':
		if(p>q)
		{
			for(i=0;i<p;i++)
			{
				c1[i]=b1[i]-b2[i];
			}
			for(i=0;i<p;i++)
			{
				if(c1[i]<0)
				{
					while(c1[i]<0)
					{
						c1[i+1]=c1[i+1]-1;
						c1[i]+=10;
					}
				}	
			}
		}
		else if(p<q)
		{
			printf("-");
			for(i=0;i<q;i++)
			{
				c1[i]=b2[i]-b1[i];
			}
			for(i=0;i<q;i++)
			{
				if(c1[i]<0)
				{
					while(c1[i]<0)
					{
						c1[i+1]=c1[i+1]-1;
						c1[i]+=10;
					}
				}
			}
		}
		else if(p==q)
		{
			k=strcmp(d1,d2);
			if(k>0)
			{
				for(i=0;i<p;i++)
			{
				c1[i]=b1[i]-b2[i];
			}
			for(i=0;i<p;i++)
			{
				if(c1[i]<0)
				{
					while(c1[i]<0)
					{
						c1[i+1]=c1[i+1]-1;
						c1[i]+=10;
					}
				}
			}
			}
			else if(k<0)
			{
				printf("-");
				for(i=0;i<q;i++)
			{
				c1[i]=b2[i]-b1[i];
			}
			for(i=0;i<q;i++)
			{
				if(c1[i]<0)
				{
					while(c1[i]<0)
					{
						c1[i+1]=c1[i+1]-1;
						c1[i]+=10;
					}
				}
			}
			}
			else if(k==0)
			{
				printf("0\n");
				continue;
			}
		}
		if(p>q)
		{
			d=p;
		}
		else
		{
			d=q;
		}
		while(c1[d]==0)d--;
		for(i=d;i>=0;i--)
		{
			printf("%d",c1[i]);
		}
		printf("\n");
		continue;
		case'*':
			for(i=0;i<p;i++)
			{
				for(j=0;j<q;j++)
				{
					c1[i+j]+=b1[i]*b2[j];
				}
			}
			k=p+q;
			for(i=0;i<=k;i++)
			{
				if(c1[i]>=10)
				{
					c1[i+1]+=c1[i]/10;
					c1[i]%=10;
				}
			}
			d=k;
			while(c1[d]==0)d--;
			if(d<0)
			{
				printf("0");
			}
			else
			{
				for(;d>=0;d--)
				{
					printf("%d",c1[d]);
				}
			}
			printf("\n");
		}
}
}
